# 숫자형?
# 숫자형(Number)이란 숫자형태로 이루어진 자료형으로, 우리가 이미 잘 알고 있는 것이다.
# 우리가 흔히 사용하는 것을 생각해보자.
# 123 같은 정수, 
# 12.34 같은 실수,
# 8진수 or 16진수 같은 것도 있다.

##
## 사칙연산
a = 3
b = 4
print(a + b)
# 7
print(a * b)
# 12
print(a / b)
# 0.75

##
## x의 y제곱을 나타내는 ** 연산자 (x 의 y 제곱 값을 돌려준다.)
a = 3
b = 4
print(a ** b)
# 81

## 나눗셈 후 '나머지'를 반환하는 % 연산자
print(7 % 3)
# 1
print(3 % 7)
# 3

# 나눗셈 후 '몫'을 반환하는 // 연산자
print(7 // 4)
# 1
# -> 몫에 해당되는 정수값 1만 돌려주다.
print( 7 / 4)
# 1.75
